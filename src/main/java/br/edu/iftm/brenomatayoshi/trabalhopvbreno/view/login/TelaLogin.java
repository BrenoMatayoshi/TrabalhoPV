    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String login = jTextField1.getText();
        String senha = String.valueOf(jPasswordField1.getPassword());
        user = usuarioController.logar(login, senha);
        if (user != null) {
            JOptionPane.showMessageDialog(this, "Login bem sucedido");
            TelaPrincipal telaPrincipal = new TelaPrincipal(user);
            telaPrincipal.preencherCategoria(telaPrincipal.jComboBox1);
//            telaPrincipal.preencherFornecedor();
            telaPrincipal.setVisible(true);
            this.dispose();
            return;
        }
        JOptionPane.showMessageDialog(this, "Login ou senha errados");
    }//GEN-LAST:event_jButton1ActionPerformed

