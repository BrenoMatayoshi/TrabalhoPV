    public void preencherTabelaListarProduto() {
        List<Produto> produtos = produtoController.produtos();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for (Produto produto : produtos) {
            if (produto.isAtivo()) {
                Object[] novaLinha = {produto.getIdProduto(), produto.getNome(), produto.getDescricao(), produto.getCategoria().getNome(), produto.getQuantidadeEstoque()};
                model.addRow(novaLinha);
            }
        }
    }

